    class tambahbarang extends Siswa1{
        int id_siswa;
        String nama,nis,kelas,jenis_kelamin;
        
        public tambahbarang() {
            this.nama = texNama.getText();
            this.nis = textNis.getText();
            this.kelas = textKelas.getText();
             this.jenis_kelamin =  (String)cbKelamin.getSelectedItem();
        }
    }
    
        
        public void refreshTable(){
            model= new DefaultTableModel();
            model.addColumn("ID Siswa");
            model.addColumn("Nama Siswa");
            model.addColumn("NIS");
            model.addColumn("Kelas");
            model.addColumn("Jenis Kelamin");
            tabelbarang.setModel(model);
            try {
                this.stat=k.getKoneksi().prepareStatement("select * from siswa");
                this.rs=this.stat.executeQuery();
                while(rs.next()){
                    Object[] data={
                        rs.getInt("id_siswa"),
                        rs.getString("nama"),
                        rs.getString("nis"),
                        rs.getString("kelas"),
                        rs.getString("jenis_kelamin"),
                    };
                    model.addRow(data);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e.getMessage());
            }
            texNama.setText("");
            textNis.setText("");
            textKelas.setText("");   
            cbKelamin.setSelectedItem("");     
        }

