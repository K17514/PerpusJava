    private void tombolgantibarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolgantibarangActionPerformed
        // TODO add your handling code here:
        try {
            tambahbarang b=new tambahbarang();
            this.stat=k.getKoneksi().prepareStatement("update siswa set nama=?,"
                    + "nis=?,kelas=?,jenis_kelamin=? where id_siswa=?");
            
            stat.setString(1, b.nama);
            stat.setString(2, b.nis);
            stat.setString(3,b.kelas);
            stat.setString(4,b.jenis_kelamin);
            stat.setInt(5, Integer.parseInt(idsiswa.getText()));
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_tombolgantibarangActionPerformed

    private void tombolsimpanbarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolsimpanbarangActionPerformed
        // TODO add your handling code here:
        try {
            tambahbarang b=new tambahbarang();
            this.stat=k.getKoneksi().prepareStatement("insert into siswa values (?,?,?,?,?)");
            stat.setInt(1, b.id_siswa);
            stat.setString(2, b.nama);
            stat.setString(3, b.nis);
            stat.setString(4,b.kelas);
            stat.setString(5,b.jenis_kelamin);
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_tombolsimpanbarangActionPerformed

